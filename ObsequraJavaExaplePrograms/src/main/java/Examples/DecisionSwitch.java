package Examples;

public class DecisionSwitch {

	public static void main(String[] args) {
		String colour ="RED";
		switch(colour)
		{
		case "Blue": System.out.println("color blue");
		break;
		case "Green" : System.out.println("Blue");
		break;
		default : System.out.println("the def color is" +colour);
		}

	}

	
}
